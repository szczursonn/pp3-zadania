public class Counter
{
    int count = 0;
    
    void increment() {
        count++;
    }
    
    void decrement() {
        count--;
    }
    
    void incrementBy10() {
        count+=10;
    }
    
    void decrementBy10() {
        count-=10;
    }
    
    void reset() {
        count = 0;
    }

}
